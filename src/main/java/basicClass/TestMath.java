package basicClass;

/**
 * @author 李昭
 */
public class TestMath {
    public static void main(String[] args) {
        System.out.println(Math.ceil(3.1));     //向上取整
        System.out.println(Math.floor(3.9));    //向下取整
        System.out.println(Math.round(3.8));    //四舍五入
        System.out.println(Math.abs(3.3));      //绝对值
        System.out.println(Math.pow(2,4));      //幂
        System.out.println(Math.PI);            //Π
        System.out.println(Math.E);             //自然底数e
        System.out.println(Math.random());      //随机数
    }
}
